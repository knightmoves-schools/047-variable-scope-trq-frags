var distanceInMiles = 100;

// Create a function called estimatedArrivalTime with no parameters
function estimatedArrivalTime() {
    // Create a local variable named milesPerHour set to 50
    var milesPerHour = 50;
    // Return distanceInMiles divided by milesPerHour
    return distanceInMiles / milesPerHour;
}

// Assign the innerHTML of the element with id 'result' to the return value from estimatedArrivalTime
